import Foundation

public extension UIScreen {
    static let mainWidth = UIScreen.main.bounds.size.width
    static let mainHeight = UIScreen.main.bounds.size.height
}

public struct Scale {
    private static let baseWidth = 375.0 //  Design width-size

    static var delta: CGFloat {
        return UIScreen.mainWidth / baseWidth - 1.0
    }
}

public extension CGFloat {
    func scale() -> CGFloat {
        let delta = floor(self * Scale.delta)
        return self + delta
    }
}

public extension Double {
    func scale() -> CGFloat {
        let curValue = CGFloat(self)
        let delta = floor(curValue * Scale.delta)
        return curValue + delta
    }
}

